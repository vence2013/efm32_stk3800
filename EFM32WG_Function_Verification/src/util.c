#include "util.h"

/******************************************************************************
 * @brief block delay
 ******************************************************************************/
void delay(unsigned int x)
{
	while ( x>0 )
	{
		x--;
	}
}

/******************************************************************************
 * @brief block delay
 ******************************************************************************/
void delay1k(unsigned int x)
{
	unsigned int i;

	while ( x>0 )
	{
		x--;
		for (i = 0; i <1000; i++) ;
	}
}
