#include "segmentlcd.h"
#include "drv.h"


/******************************************************************************
 * @brief
 *****************************************************************************/
void lcd_setup( void )
{
	SegmentLCD_Init( false );
	LCD_BiasSet( lcdBiasOneFourth );
	LCD_ContrastSet( 15 );
}

/******************************************************************************
 * @brief
 *****************************************************************************/
void Drv_setup( void )
{
	usart_setup();

	lcd_setup();
}
